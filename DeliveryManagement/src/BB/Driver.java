package BB;

public class Driver {

	private int id;
	private String name;
	private String time;
	
	public Driver(int id, String name, String time) {
		
		this.id = id;
		this.name = name;
		this.time = time;
		
	}	

	
	
	public int getId() {
		return id;
	}

	public String getName() {
		return name;
	}



	public String getTime() {
		return time;
	}
}

